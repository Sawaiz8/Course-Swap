class Customer:

    def __init__(self, name, email, supply, demand):
        self.name = name
        self.email = email
        self.supply = supply
        self.demand = demand

    def getName(self):
        return self.name

    def getEmail(self):
        return self.email

    def getSupply(self):
        return self.supply

    def getDemand(self):
        return self.demand


    
